package com.madira.Model;

public class DryDay_Model {
    String Id;
    String Day_name;
    String Date;

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getDay_name() {
        return Day_name;
    }

    public void setDay_name(String day_name) {
        Day_name = day_name;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }
}
